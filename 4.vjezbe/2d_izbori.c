#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#define SIZE 100

int main() {
    int A[SIZE][SIZE], B[SIZE][SIZE];
    int N, M;
    int najveciNetoRedak = -1, najveciNetoStupac = -1, RazlikaSum;
    int indeksNajvecegRetka = -1, indeksNajvecegStupca = -1;

    scanf("%d %d", &N, &M);

    if (N > SIZE || M > SIZE || N <= 0 || M <= 0) {
        return 1;
    }

    for (int row = 0; row < N; row++) {
        for (int col = 0; col < M; col++) {
            scanf("%d", &A[row][col]);
        }
    }

    for (int row = 0; row < N; row++) {
        for (int col = 0; col < M; col++) {
            scanf("%d", &B[row][col]);
        }
    }

    for (int i = 0; i < N; i++) {
        RazlikaSum = 0;
        for (int j = 0; j < M; j++) {
            RazlikaSum += A[i][j] - B[i][j];
        }
        if (najveciNetoRedak < RazlikaSum) {
            najveciNetoRedak = RazlikaSum;
            indeksNajvecegRetka = i;
        }
    }

    for (int j = 0; j < M; j++) {
        RazlikaSum = 0;
        for (int i = 0; i < N; i++) {
            RazlikaSum += A[i][j] - B[i][j];
        }
        if (najveciNetoStupac < RazlikaSum) {
            najveciNetoStupac = RazlikaSum;
            indeksNajvecegStupca = j;
        }
    }

    if (najveciNetoRedak >= najveciNetoStupac) {
        printf("Najbolje je ukloniti %d. red iz tablica.\n", indeksNajvecegRetka + 1);
    }
    else {
        printf("Najbolje je ukloniti %d. stupac iz tablica.\n", indeksNajvecegStupca + 1);
    }

    return 0;
}
